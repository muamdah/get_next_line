#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*fresh;

	fresh = malloc(sizeof(size));
	if (!fresh || size > 2147493647)
		return (NULL);
	ft_bzero(fresh, size);
	return (fresh);
}
